package Sec1;

public class Calculator {
    double add(double a,double b){
        return a+b;
    }
    static double multiply(double a,double b){
        return a*b;
    }
    static double  minus(double a,double b){
        return a-b;
    }
    static double divide(double a,double b){
        return a/b;
    }
}
